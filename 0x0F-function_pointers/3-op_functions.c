/**
 * op_add - sum two integers
 * @a: integer
 * @b: integer
 * Return: an integer
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract two integers
 * @a: integer
 * @b: integer
 * Return: an integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two integers
 * @a: integer
 * @b: integer
 * Return: an integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two integers
 * @a: integer
 * @b: integer
 * Return: an integer
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of the division of two integers
 * @a: integer
 * @b: integer
 * Return: an integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
